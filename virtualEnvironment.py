# A virtul enviroment is the tool used to isolate specic python environment on a single machine
# allowing you to work on multiple projects with different depandancys and packages without 
# conflexs 
# to make a VE 
# 1) make a new folder 
# 2) open termal and write  "python3 -m venv myenv"
# it will make a copy of the python present in computer and all the package we install in it 
# are different from the original python

# to activate the VE type in termal "source myenv/bin/activate"
